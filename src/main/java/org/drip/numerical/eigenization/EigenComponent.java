/**
 * <i>EigenComponent</i> holds the Component's Eigenvector and the corresponding Eigenvalue.
 * 
 * <br><br>
 *  <ul>
 *		<li><b>Module </b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/ComputationalCore.md">Computational Core Module</a></li>
 *		<li><b>Library</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/NumericalAnalysisLibrary.md">Numerical Analysis Library</a></li>
 *		<li><b>Project</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/numerical">Numerical Quadrature, Differentiation, Eigenization, Linear Algebra, and Utilities</a></li>
 *		<li><b>Package</b> = <a href = "https://github.com/lakshmiDRIP/DROP/tree/master/src/main/java/org/drip/numerical/eigenization">Eigen-value and Eigen-component Extraction Schemes</a></li>
 *  </ul>
 * <br><br>
 *
 * @author Lakshmi Krishnamurthy
 */

public class EigenComponent
{
	private double[] _eigenVector = null;
	private double _eigenValue = java.lang.Double.NaN;

	/**
	 * EigenComponent Constructor
	 * 
	 * @param eigenVector The Eigenvector
	 * @param eigenValue The Eigenvalue
	 * 
	 * @throws java.lang.Exception Thrown if the Inputs are invalid
	 */

	public EigenComponent (
		final double[] eigenVector,
		final double eigenValue)
		throws java.lang.Exception
	{
		if (null == (_eigenVector = eigenVector) ||
			!org.drip.numerical.common.NumberUtil.IsValid (
				_eigenValue = eigenValue
			)
		)
		{
			throw new java.lang.Exception (
				"EigenComponent ctr: Invalid Inputs"
			);
		}

		int eigenComponentCount = _eigenVector.length;

		if (0 == eigenComponentCount)
		{
			throw new java.lang.Exception (
				"EigenComponent ctr: Invalid Inputs"
			);
		}

		for (int eigenComponentIndex = 0;
			eigenComponentIndex < eigenComponentCount;
			++eigenComponentIndex)
		{
			if (!org.drip.numerical.common.NumberUtil.IsValid (
				_eigenVector[eigenComponentIndex]
			))
			{
				throw new java.lang.Exception (
					"EigenComponent ctr: Invalid Inputs"
				);
			}
		}
	}

	/**
	 * Retrieve the Eigenvalue
	 * 
	 * @return The Eigenvalue
	 */

	public double eigenValue()
	{
		return _eigenValue;
	}

	/**
	 * Retrieve the Eigenvector
	 * 
	 * @return The Eigenvector
	 */

	public double[] eigenVector()
	{
		return _eigenVector;
	}
}
